package ua.hillel.java.elementary1.arrays.tasks;

/**
 * Some sorts of the arrays.
 */
public interface Sorts {
    /**
     * Bubble sort.
     *
     * @param array the array
     */
    void bubbleSort(int[] array);

    /**
     * Min sort.
     *
     * @param array the array
     */
    void minSort(int[] array);

    /**
     * Merge sort.
     *
     * @param array the array
     */
    void mergeSort(int[] array);
}
